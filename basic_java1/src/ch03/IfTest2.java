package ch03;

public class IfTest2 {

    // 메인함수(코드의 진입점)
    public static void main(String[] args) {

        int age = 10;
        // if else -> 반드시 하나는 수행이 된다.
        if (age >= 20) {
            System.out.println("쫌 아저씨네요!");
        } else {
            System.out.println("아직 쫌 어리네요!");
        }

    } // end of main
} // end of class
